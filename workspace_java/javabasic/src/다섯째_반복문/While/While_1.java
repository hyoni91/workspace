package 다섯째_반복문.While;

/*
    [while 문법 --> ~동안 ]

    while(반복 조건){
        반복적으로 실행되는 내용.
    }

*/

public class While_1 {
    public static void main(String[] args) {

//        반복 시작점
        int i = 1;


//         반복 조건
        while (i <= 3){
            System.out.println(1);
            i++;  //반복을 파기할 조건 (조건이 안 맞을때 까지 반복 실행함)
        }

    }
}
